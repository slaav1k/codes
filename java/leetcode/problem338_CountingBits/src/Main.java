public class Main {
    public static void main(String[] args) {
        System.out.println(Arrays.toString(countBits(5)));
    }


    public static int[] countBits(int n) {
        int[] res = new int[n + 1];
        for (int i = 1; i <= n; i++) {
            res[i] = res[i & (i - 1)] + 1;
        }
        return res;
    }
}